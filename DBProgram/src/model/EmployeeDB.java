package model;

public class EmployeeDB {
	private int id;
	private String name;
	private String dept;
	private int salary;
	private String date;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDept() {
		return dept;
	}

	public void setDept(String dept) {
		this.dept = dept;
	}

	public int getSalary() {
		return salary;
	}

	public void setSalary(int salary) {
		this.salary = salary;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	@Override
	public String toString() {
		return "EmployeeDB [id=" + id + ", name=" + name + ", dept=" + dept + ", salary=" + salary + ", date=" + date
				+ "]";
	}

}
